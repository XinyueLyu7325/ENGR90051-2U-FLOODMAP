const packs: Record<Language, TranslationPack> = {
  en,
  vi: { ...en, ...vi, ...coverage.vi },
  "zh-Hant": { ...en, ...zhHant, ...coverage["zh-Hant"] },
  "zh-Hans": { ...en, ...zhHans, ...coverage["zh-Hans"] },
  el: { ...en, ...el, ...coverage.el },
  es: { ...en, ...es, ...coverage.es },
};

export const translations = Object.fromEntries(
  Object.keys(en).map((key) => [
    key,
    {
      en: packs.en[key],
      vi: packs.vi[key],
      "zh-Hant": packs["zh-Hant"][key],
      "zh-Hans": packs["zh-Hans"][key],
      el: packs.el[key],
      es: packs.es[key],
    },
  ]),
) as Record<keyof typeof en, Record<Language, TranslationValue>>;

export function isSupportedLanguage(lang: string | null): lang is Language {
  return !!lang && LANGUAGES.some((item) => item.code === lang);
}

export function t(
  key: keyof typeof translations | string,
  lang: Language,
  ...args: any[]
): string {
  const safeLang = isSupportedLanguage(lang) ? lang : "en";
  const value = translations[key as keyof typeof translations]?.[safeLang] ?? key;
  if (typeof value === "function") {
    return value(...args);
  }
  return value;
}
